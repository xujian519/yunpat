#!/usr/bin/env node

/**
 * 解析专利考试真题 PDF，输出 JSON 和 Markdown 文件
 */

const fs = require('fs')
const path = require('path')
const pdfParse = require(require('path').resolve(__dirname, '../packages/document-tools/node_modules/pdf-parse'))

const inputPath = process.argv[2]
if (!inputPath) {
  console.error('用法: node scripts/parse-exam-pdf.cjs <pdf文件路径>')
  process.exit(1)
}

if (!fs.existsSync(inputPath)) {
  console.error(`文件不存在: ${inputPath}`)
  process.exit(1)
}

const absInputPath = path.resolve(inputPath)
const outDir = path.dirname(absInputPath)
const baseName = path.basename(absInputPath, '.pdf')

console.log(`解析: ${absInputPath}`)

// 1. 解析 PDF
const dataBuffer = fs.readFileSync(absInputPath)
pdfParse(dataBuffer).then((data) => {
  console.log(`页数: ${data.numpages}, 文本长度: ${data.text.length} 字`)

  // 2. 清洗文本：去掉 Foxit 水印
  const cleanText = data.text
    .replace(/Generated by Foxit PDF Creator.*?\n/g, '')
    .replace(/http:\/\/www\.foxitsoftware\.com.*?\n/g, '')
    .replace(/For evaluation only\..*?\n/g, '')
    .replace(/\n{3,}/g, '\n\n')
    .trim()

  // 3. 提取结构化段落
  const lines = cleanText.split('\n')
  const elements = []
  let currentPage = null

  for (const line of lines) {
    const trimmed = line.trim()
    if (!trimmed) continue

    // 检测页码标记（纯数字行）
    if (/^\d{1,2}$/.test(trimmed) && parseInt(trimmed) <= data.numpages) {
      currentPage = parseInt(trimmed)
      continue
    }

    // 检测标题
    if (/^200\d年/.test(trimmed) || /^试题说明|^答题须知/.test(trimmed)) {
      elements.push({ type: 'title', content: trimmed, page: currentPage })
    } else if (/^第[一二]题/.test(trimmed)) {
      elements.push({ type: 'heading', level: 2, content: trimmed, page: currentPage })
    } else if (
      /^(权\s*利\s*要\s*求\s*书|说\s*明\s*书|技术领域|背景技术|发明内容|附图的简要说明|具体实施方式|摘要)/.test(
        trimmed
      )
    ) {
      elements.push({ type: 'heading', level: 3, content: trimmed, page: currentPage })
    } else if (/^\d+．/.test(trimmed) || /^\d+\.\s/.test(trimmed)) {
      elements.push({ type: 'list_item', content: trimmed, page: currentPage })
    } else {
      elements.push({ type: 'paragraph', content: trimmed, page: currentPage })
    }
  }

  // 4. 构建 JSON
  const jsonResult = {
    metadata: {
      source: path.basename(absInputPath),
      pages: data.numpages,
      textLength: cleanText.length,
      parseTime: new Date().toISOString(),
      elementsCount: elements.length,
    },
    elements,
    fullText: cleanText,
  }

  const jsonPath = path.join(outDir, `${baseName}.json`)
  fs.writeFileSync(jsonPath, JSON.stringify(jsonResult, null, 2), 'utf-8')
  console.log(`JSON 已输出: ${jsonPath}`)

  // 5. 构建 Markdown
  const mdLines = []
  mdLines.push(`# ${baseName.replace(/_/g, ' ')}`)
  mdLines.push('')
  mdLines.push(
    `> 源文件: ${path.basename(absInputPath)} | 页数: ${data.numpages} | 解析时间: ${new Date().toISOString()}`
  )
  mdLines.push('')

  for (const el of elements) {
    switch (el.type) {
      case 'title':
        mdLines.push(`## ${el.content}`)
        mdLines.push('')
        break
      case 'heading':
        mdLines.push(`${'#'.repeat(el.level)} ${el.content}`)
        mdLines.push('')
        break
      case 'list_item':
        mdLines.push(`${el.content}`)
        mdLines.push('')
        break
      case 'paragraph':
      default:
        mdLines.push(el.content)
        mdLines.push('')
        break
    }
  }

  const mdPath = path.join(outDir, `${baseName}.md`)
  fs.writeFileSync(mdPath, mdLines.join('\n'), 'utf-8')
  console.log(`Markdown 已输出: ${mdPath}`)
  console.log('完成!')
})
